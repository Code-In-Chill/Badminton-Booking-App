    /**
     * 
     * @return khi bấm nhận hàng sẽ tự động tạo mã hóa đơn 
     */
    public String taoMaHD() {
        int count = dao_HoaDon.loadMaHDCount();
        count++;
        // Tạo mã hóa đơn theo quy tắc và có thứ tự
        return "HD" + String.format("%03d", count); // Ví dụ: HD001, HD002,...
    }

