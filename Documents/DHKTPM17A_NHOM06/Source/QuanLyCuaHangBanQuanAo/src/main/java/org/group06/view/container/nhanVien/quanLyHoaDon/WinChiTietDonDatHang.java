    public int getSoLuongDaDat() {
        if (tblDSQuanAo.getSelectedRow() == -1) {
            return -1;
        } else {
            int sl = (int) tblDSQuanAo.getValueAt(tblDSQuanAo.getSelectedRow(), 4);
            return sl;
        }
    }
    
